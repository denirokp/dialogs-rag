#!/usr/bin/env python3
"""
Регулярные выражения для фильтрации диалогов
"""

import re

# Ключевые слова доставки
DELIVERY_ANY = re.compile(
    r"(пвз|пункт выдачи|постамат\w*|курьер\w*|самовывоз\w*|сдэк|boxberry|боксберри|доставк\w+|трек(?:-?номер)?|наложенн\w+ платеж|возврат\w+)", 
    re.IGNORECASE
)

# Платформенный шум (не связанный с доставкой)
PLATFORM_NOISE = re.compile(
    r"(реклам\w+|продвижен\w+|подписк\w+|тариф(ы|ом|ов)?|алгоритм\w+)", 
    re.IGNORECASE
)

# Маркеры барьеров
BARRIER_MARK = re.compile(
    r"(дорог\w+|комисси\w+|сложн\w+|не поним\w+|жалоб\w+|проблем\w+|сбой\w+|возврат\w+|претензи\w+|нет\s+(пвз|доставк\w+|логистик\w+))", 
    re.IGNORECASE
)

# Маркеры идей
IDEA_MARK = re.compile(
    r"(идея|хотелось бы|было бы удобно|нужно добавить|предлагаю|можно сделать)", 
    re.IGNORECASE
)

# Односложные ответы
YESNO = re.compile(
    r"^(да|нет|угу|ага|не знаю|наверное|возможно)[.!?]?$", 
    re.IGNORECASE
)
